# 키보드에서 정수로 된 돈의 액수를 입력받아 50000원권, 10000원권, 5000원권, 1000원권, 500원동전, 100원동전, 50원동전, 1원동전 각 몇 개로 변환 되는지 작성 하세요.  

# 금액 입력받기
amount = int(input("금액을 입력하세요: "))

# 각 화폐 단위 정의 (큰 단위부터 작은 단위로)
money = [50000, 10000, 5000, 1000, 500, 100, 50, 10, 5, 1]

# 각 화폐 단위에 대한 결과 계산 및 출력
for coin in money:
    count = amount // coin  # 해당 화폐 단위의 개수 계산
    amount %= coin  # 남은 금액 갱신
    print(f"{coin}원: {count}개")
